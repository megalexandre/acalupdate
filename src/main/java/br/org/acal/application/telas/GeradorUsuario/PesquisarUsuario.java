    private void configurarTabela(){
        
         DefaultTableCellRenderer centralizar = new DefaultTableCellRenderer();
         centralizar.setHorizontalAlignment(SwingConstants.CENTER);
        
         jTable1.setModel(new DefaultTableModel(
                new Object[][]{}, new String[]{"Usuário","Tabela","Privilégios"}){
                
                Class[] types = new Class[]{String.class, String.class, String.class};
                
                
                @Override
                public Class getColumnClass(int columnIndex){
                    return types[columnIndex];
                    
                }
                
                boolean[] canEdit =  new boolean[]{false, false, false};
        
                @Override
                public boolean isCellEditable(int rowIndex, int columnIndex){
                    
                    return canEdit[columnIndex];
                }
                    
        });
        
        jTable1.getColumn("Usuário").setCellRenderer(centralizar);
        jTable1.getColumn("Tabela").setCellRenderer(centralizar);
        jTable1.getColumn("Privilégios").setCellRenderer(centralizar);
        
        preencherTabela();
    }
    
    
    private void preencherTabela(){
        
        DefaultTableModel model = (DefaultTableModel)jTable1.getModel();
        model.setRowCount(0);
        
        List<TablesPriv> usuarios = new DaoUsuario().BuscaTodosUsuarios();
        
        if(!usuarios.isEmpty()){
            
            for(TablesPriv u : usuarios){
                
                String temp = u.getTablepriv();
                temp = temp.replaceAll( "Select", "Selecionar");
                temp = temp.replaceAll( "Insert", "Inserir");
                temp = temp.replaceAll( "Update", "Atualizar");
                temp = temp.replaceAll( "Delete", "Excluir");
                
                model.addRow(new Object[]{u.getTablesPrivPK().getUser(),u.getTablesPrivPK().getTablename(),temp});
            }
            
        }else{
            
            JOptionPane.showMessageDialog(this,"Não existem usuários cadastrados além do Administrador","Atenção",JOptionPane.INFORMATION_MESSAGE);
        }
        
    }
